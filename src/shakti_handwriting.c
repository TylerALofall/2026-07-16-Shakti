#define FONT_COUNT (sizeof(FONT) / sizeof(FONT[0]))

static const shakti_font_entry_t *find_entry(unsigned char character)
{
    size_t index;

    for (index = 0U; index < FONT_COUNT; ++index) {
        if (FONT[index].character == character) {
            return &FONT[index];
        }
    }

    return NULL;
}

int shakti_handwriting_render_char(
    unsigned char character,
    char rows[8][9]
)
{
    const shakti_font_entry_t *entry;
    size_t row;
    size_t column;

    if (rows == NULL) {
        return 0;
    }

    entry = find_entry(character);

    if (entry == NULL) {
        return 0;
    }

    for (row = 0U; row < 8U; ++row) {
        for (column = 0U; column < 8U; ++column) {
            rows[row][column] = '.';
        }

        rows[row][8] = '\0';
    }

    for (row = 0U; row < 7U; ++row) {
        for (column = 0U; column < 5U; ++column) {
            if (entry->rows[row][column] == '1') {
                rows[row][column + 1U] = '#';
            }
        }
    }

    return 1;
}

int shakti_handwriting_supports_text(const char *text)
{
    const unsigned char *cursor;

    if (text == NULL || text[0] == '\0') {
        return 0;
    }

    cursor = (const unsigned char *)text;

    while (*cursor != '\0') {
        if (find_entry(*cursor) == NULL) {
            return 0;
        }

        cursor++;
    }

    return 1;
}

int shakti_handwriting_write_text_artifact(
    const char *text,
    const char *path
)
{
    FILE *file;
    const unsigned char *cursor;
    size_t character_index;
    int success;

    if (text == NULL ||
        path == NULL ||
        !shakti_handwriting_supports_text(text)) {
        return 0;
    }

    file = fopen(path, "w");

    if (file == NULL) {
        return 0;
    }

    success =
        fprintf(file, "SHAKTI_WRITTEN_TEXT_8X8_V1\n") > 0 &&
        fprintf(file, "TEXT=%s\n", text) > 0 &&
        fprintf(file, "CHARACTERS=%lu\n",
                (unsigned long)strlen(text)) > 0;

    cursor = (const unsigned char *)text;
    character_index = 0U;

    while (success && *cursor != '\0') {
        char rows[8][9];
        size_t row;

        if (!shakti_handwriting_render_char(*cursor, rows)) {
            success = 0;
            break;
        }

        success =
            fprintf(
                file,
                "CHARACTER=%lu ASCII=%u TEXT=%c\n",
                (unsigned long)character_index,
                (unsigned int)*cursor,
                *cursor
            ) > 0;

        for (row = 0U; success && row < 8U; ++row) {
            success = fprintf(file, "%s\n", rows[row]) > 0;
        }

        cursor++;
        character_index++;
    }

    if (success) {
        success = fflush(file) == 0;
    }

    if (fclose(file) != 0) {
        success = 0;
    }

    return success;
}
